# Базова математика
puts 10 + 5  # Додавання: 15
puts 10 - 5  # Віднімання: 5
puts 10 * 2  # Множення: 20

# Ділення 
puts 10 / 3  # Виведе 3! Оскільки обидва числа цілі (Integer)
puts 10.0 / 3 # Виведе 3.3333. Якщо хоча б одне число дробове (Float), результат буде точним.

# % — Остача від ділення (Модуль)
puts 10 % 3  # Виведе 1 

# ** — Піднесення до ступеня 

puts 2 ** 3  # 2 у кубі = 8
puts 5 ** 2  # 5 у квадраті = 25